# Pregunta 3:
# Que es INDEX (indice) en base de datos, indicar beneficios y desventajas



# Que es index:
#     Es una estructura de datos que sirve para localizar rapidamente
#     las filas de una tabla en por el contenido de la columna indexada,
#     puede ser aplicada a una o mas columnas.

# Beneficios:
#     Mejora el rendimiento de una consulta a una base de datos

# Desventajas:
#     Los indices ocupan espacio adicional.

#     Cuando se agregan nuevos registros en una tabla con indices
#     es necesario actualizar todos los indices nuevamente
